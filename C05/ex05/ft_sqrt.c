int	ft_sqrt(int nb)
{
	long int	i;

	i = 1;
	if (nb == 1)
		return (1);
	if (nb > 1)
	{
		while (i * i <= nb)
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
